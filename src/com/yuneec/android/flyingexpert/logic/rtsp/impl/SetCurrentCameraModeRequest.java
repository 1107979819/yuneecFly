public class SetCurrentCameraModeRequest extends RtspRequest {


	private int mode;
	private String currentMode = "video";
	
	
	public SetCurrentCameraModeRequest(int mode) {
		this.mode = mode;
		switch (mode) {
		case 2:
			currentMode = "photo";
			break;
		case 1:
			currentMode = "video";
			break;
		default:
			break;
		}
	}
	
	
	@Override
	public void createRequestBody() throws JSONException {
		
		LogX.i("test_send", "current request is : "+getClass().getSimpleName());
		
	}

	
	
	
	@Override
	public void parseResponse() throws JSONException {
		JSONObject rootObject = new JSONObject(responseContent);
		LogX.i("test_receive", responseContent);
		resultCode = Integer.valueOf(rootObject.getInt(RequestKey.RESULT_CODE));
		if (resultCode == 0) {
			
		} else {
			errorMsg = responseContent;
		}
		
	}

	
	
	@Override
	public String getUrl() {
		return BASE_URL+RTSPCommand.URL_SET_CAMERA_MODE+"&mode="+currentMode;
	}


	public int getMode() {
		return mode;
	}


}
